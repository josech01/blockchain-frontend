export const checkLevel = (amount) => {
  // console.log(amount);
  const result = leagueData.find((item, index) => {
    if (index == 0 && amount > item.balance) {
      return true;
    }
    return amount >= item.balance && amount < leagueData[index - 1]?.balance;
  });
  // console.log(result);
  const itemIndex = leagueData.indexOf(result);
  if (itemIndex > 0) return { ...result, itemIndex };
  else return { ...result };
};

export const toUSDFormat = (number, digit = 2) => {
  if (number)
    return Number(number).toLocaleString('en-US', {
      maximumFractionDigits: digit,
    });
  return 0;
};

export const walletAddressConvert = (str, length = 5) => {
  return str.slice(0, length) + '...' + str.slice(str.length - length, str.length);
};

export const convertUSDToString = (number) => {
  let result = number / 1000000;
  if (result >= 1) return result + 'M';
  else if (result < 1) {
    return number / 1000 + 'k';
  }
};

