export const ceilDecimal = (value, decimal) => {
  const k = Math.pow(10, decimal);
  // console.log({ value, k });
  // console.log(Math.ceil(parseFloat(value) * k) / k);
  return Math.ceil(parseFloat(value) * k) / k;
};

export const convertTime = () => {
  const endDate = new Date(Date.UTC(2023, 7, 27, 16));
  const currentTime = new Date();
  let timeLeft = (endDate.getTime() - currentTime.getTime()) / 1000;
  // console.log(timeLeft);

  let day, hour;
  // console.log(timeLeft);

  day = Math.floor(timeLeft / (24 * 3600));

  hour = Math.floor((timeLeft % (24 * 3600)) / 3600);
  if (timeLeft > 0) return `${day} days & ${hour} hours left`;
  else {
    return '0 days & 0 hours left';
  }
};
